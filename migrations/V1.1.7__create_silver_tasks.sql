-- ---- WEEKLY MERGE TASKS ----

CREATE TASK IF NOT EXISTS ECOMMERCE_DB.SILVER_LAYER.TASK_MERGE_CUSTOMERS
    WAREHOUSE = SNOWFLAKE_LEARNING_WH
    SCHEDULE  = 'USING CRON 0 2 * * SUN America/New_York'
    WHEN SYSTEM$STREAM_HAS_DATA('ECOMMERCE_DB.BRONZE_LAYER.CUSTOMER_RAW_STREAM')
AS
MERGE INTO ECOMMERCE_DB.SILVER_LAYER.CUSTOMERS tgt
USING (
    SELECT
        CUSTOMER_ID,
        CUSTOMER_UNIQUE_ID,
        TRIM(CUSTOMER_ZIP_CODE_PREFIX) AS ZIP_CODE,
        INITCAP(TRIM(CUSTOMER_CITY))   AS CITY,
        UPPER(TRIM(CUSTOMER_STATE))    AS STATE
    FROM ECOMMERCE_DB.BRONZE_LAYER.CUSTOMER_RAW_STREAM
    QUALIFY ROW_NUMBER() OVER (PARTITION BY CUSTOMER_ID ORDER BY CUSTOMER_ID) = 1
) src
ON tgt.CUSTOMER_ID = src.CUSTOMER_ID
WHEN MATCHED THEN UPDATE SET
    tgt.CUSTOMER_UNIQUE_ID = src.CUSTOMER_UNIQUE_ID,
    tgt.ZIP_CODE           = src.ZIP_CODE,
    tgt.CITY               = src.CITY,
    tgt.STATE              = src.STATE,
    tgt.UPDATED_AT         = CURRENT_TIMESTAMP()
WHEN NOT MATCHED THEN INSERT
    (CUSTOMER_ID, CUSTOMER_UNIQUE_ID, ZIP_CODE, CITY, STATE, LOADED_AT, UPDATED_AT)
VALUES
    (src.CUSTOMER_ID, src.CUSTOMER_UNIQUE_ID, src.ZIP_CODE, src.CITY, src.STATE,
     CURRENT_TIMESTAMP(), CURRENT_TIMESTAMP());

