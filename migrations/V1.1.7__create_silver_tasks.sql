CREATE TASK IF NOT EXISTS ECOMMERCE_DB.SILVER_LAYER.TASK_MERGE_PRODUCTS
    WAREHOUSE = SNOWFLAKE_LEARNING_WH
    SCHEDULE  = 'USING CRON 0 2 * * SUN America/New_York'
    WHEN SYSTEM$STREAM_HAS_DATA('ECOMMERCE_DB.BRONZE_LAYER.PRODUCTS_RAW_STREAM')
AS
MERGE INTO ECOMMERCE_DB.SILVER_LAYER.PRODUCTS tgt
USING (
    SELECT
        p.PRODUCT_ID,
        COALESCE(t.PRODUCT_CATEGORY_NAME_ENGLISH, p.PRODUCT_CATEGORY_NAME) AS PRODUCT_CATEGORY,
        p.PRODUCT_WEIGHT_G,
        p.PRODUCT_LENGTH_CM,
        p.PRODUCT_HEIGHT_CM,
        p.PRODUCT_WIDTH_CM,
        ROUND(p.PRODUCT_LENGTH_CM * p.PRODUCT_HEIGHT_CM * p.PRODUCT_WIDTH_CM, 2) AS VOLUME_CM3
    FROM ECOMMERCE_DB.BRONZE_LAYER.PRODUCTS_RAW_STREAM p
    LEFT JOIN ECOMMERCE_DB.BRONZE_LAYER.PRODUCT_CATEGORY_TRANSLATION_RAW t
      ON p.PRODUCT_CATEGORY_NAME = t.PRODUCT_CATEGORY_NAME
) src
ON tgt.PRODUCT_ID = src.PRODUCT_ID
WHEN MATCHED THEN UPDATE SET
    tgt.PRODUCT_CATEGORY  = src.PRODUCT_CATEGORY,
    tgt.PRODUCT_WEIGHT_G  = src.PRODUCT_WEIGHT_G,
    tgt.PRODUCT_LENGTH_CM = src.PRODUCT_LENGTH_CM,
    tgt.PRODUCT_HEIGHT_CM = src.PRODUCT_HEIGHT_CM,
    tgt.PRODUCT_WIDTH_CM  = src.PRODUCT_WIDTH_CM,
    tgt.VOLUME_CM3        = src.VOLUME_CM3,
    tgt.UPDATED_AT        = CURRENT_TIMESTAMP()
WHEN NOT MATCHED THEN INSERT
    (PRODUCT_ID, PRODUCT_CATEGORY, PRODUCT_WEIGHT_G, PRODUCT_LENGTH_CM,
     PRODUCT_HEIGHT_CM, PRODUCT_WIDTH_CM, VOLUME_CM3, LOADED_AT, UPDATED_AT)
VALUES
    (src.PRODUCT_ID, src.PRODUCT_CATEGORY, src.PRODUCT_WEIGHT_G, src.PRODUCT_LENGTH_CM,
     src.PRODUCT_HEIGHT_CM, src.PRODUCT_WIDTH_CM, src.VOLUME_CM3,
     CURRENT_TIMESTAMP(), CURRENT_TIMESTAMP());

